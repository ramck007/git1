package seuzz;

public class seuzz44abcd {
    public static void main(String[]args){

        System.out.print("UPPER CASE : ");
        char ABCD='A';
        for (int i = 1;i<=26;i++){
            System.out.print(ABCD+" ");
            ABCD++;
        }
        System.out.print("\nlower case : ");
        char abcd='a';
        for (int i = 1;i<=26;i++) {
            System.out.print(abcd+ " ");
            abcd++;
        }

    }
}
