package seuzz;

public class seuzz17unoperators{
    public static void main(String[]args){
        int i = 5;

        //prints 5 post increment
        System.out.println(i++);
        // checking if incremented
        System.out.println(i);
        //prints 6 pre increment
        System.out.println(++i);
        //prints 13 unary minus -6
        System.out.println(-i);
        // print unary plus
        System.out.println(+i);

    }
}
